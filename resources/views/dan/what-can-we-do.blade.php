<h1>Hvorfor kan vi ikke bare alle enes om én beregning?</h1>
<p>Påskens tidspunkt har intet at gøre med dogmer eller teologi; den har at gøre med, hvordan vi måler tid!</p>

<p>Vores syndige splittelse gør, at Jesus, ad mystisk vej, lider, fordi vi ikke tillader Hans Helligånd at bidrage til den enhed, som Jesus tilvejebragte ved at forene Sin Familie ved sin Lidelse, Død og Opstandelse.</p>

<p>Hvis vi var i stand til det, eller hvis vi blev givet mulighed for at gøre noget ved dette og have ét, fælles tidspunkt for påske, således at alle kristne i hele verden ville stå sammen i fejringen af opstandelsen – ville vi så vælge at gøre det?</p>

<p>Hvis du svarer ”ja” til dette spørgsmål, så læs endelig videre og overvej, om du er parat til at tage et lille skridt i retning af at lade din stemme blive hørt i bestræbelsen på at hele de sår, som Kirken lider under ved vores splittelse.</p>

<h2>Tag et lille skridt i retning af enhed</h2>
<p><b>Første skridt:</b>  En generel appel underskrives og samles med den forventede én million underskrifter.</p>

<p><b>Andet skridt:</b> Med en underskrevet appel, vil en brev-skabelon blive tilgængelig for de enkelte, der er ønsker at inddrage deres respektive biskop / eller tilsvarende indenfor deres konfession, der har indflydelse i de anliggender, som den udtrykte appel drejer sig om.</p>


<h2>Hvordan vil min stemme tælle og hvad vil denne appel opnå?</h2>
<p>Fordi vores hensigt med denne webside er at give lægfolk mulighed for at udfolde deres dåbsnåde, som Gud åbenbarede i 1. Petersbrev 2,-10: ”Men I er en udvalgt slægt, et kongeligt præsteskab, et helligt folk, et ejendomsfolk, for at I skal forkynde Hans Guddomsmagt, Han som kaldte jer ud af mørket til sit underfulde lys, I, som ikke før var et folk, men nu er Guds folk, I, som ikke fandt barmhjertighed, men nu har fundet barmhjertighed”.</p>

<p>Dette ophæver ikke den anden af Guds nådegaver; de ordinerede biskopper / eller tilsvarende i de respektive konfessioner, men dette er lægfolk, der udfolder deres gudgivne dåbs-nådegave, som Gud også har givet til Sin Kirke.</p>

<p style="font-style:italic">”Lægfolket har ret og pligt til at udøve apostolatet i kraft af selve dets forening med Kristus, Kirkens Hoved. Indføjet i Kristi Mystiske Legeme ved dåben og styrket med Helligåndens kraft ved firmelsen, får lægfolket Apostolatet overgivet af Herren selv”.(Punkt 3, Lægfolkets Apostolat, Andet Vatikan Koncil).</p>

<p>Denne appel fra kristne udover hele verden er kristne, der udfolder deres apostolat i Kristi Kirkes liv.</p>

<p>”Kristus er den store profet, som både ved sit livs vidnesbyrd og i kraft af sit ord har forkyndt Faderens Rige, og indtil den fulde åbenbaring af Guds herlighed opfylder han sit profetiske embede ikke alene gennem hierarkiet, som lærer i Hans navn og med Hans autoritet, men også gennem lægfolk, som Han af den grund gør til vidner og udstyrer med troens sans og ordets nåde (jf. Ap.G. 2,17-18; Åb.19,10)”. (Punkt 35, Konstitution om Kirken, Lumen Gentium, Andet Vatikan Koncil).</p>

<p>Denne appel er også lægfolket ud over hele verden, der udfolder deres undervisende (profetiske) rolle i Kristi kKrkes liv sammen med hierarkiet.</p>

<p>Det er derfor ikke bare nødvendigt at have et bevis i form af underskrifter, men at appellen derudover, af medlemmerne af de forskellige konfessioner, der udgør Kirken, bliver givet til de respektive gejstlige i dens hierarki.</p>

<p>It is therefore not only necessary to have the proof in form of the signatures, but that the petition is additionally taken by members of the different denominations that make up the church to respective clerics in its hierarchy. This will be done in the hope that this will serve as a leverage of change by these clerics then having evidence that there is a desire from the people of Christ to demonstrate unity of the heart and celebrate this great feast as one Christian family, not splintered, fragmented and divided.</p>


<p>Dette vil blive gjort i håbet om, at dette vil tjene som et redskab til forandring for disse gejstlige, der således har et vidnesbyrd om, at der er et ønske fra Kristi folk om at udvise enhed i hjertet og fejre den store fest som én kristen familie, ikke splintret, fragmenteret og delt.</p>

<p>EEnhver som har underskrevet appellen og indvilget i at blive kontaktet, vil få henvendelse pr. e-mail, når forskellige milepæle bliver nået, f.eks. 10.000, 100.000 underskrifter. Når et afgørende antal er blevet opnået, kan de, der har indvilget i at kontakte deres lokale biskop / eller tilsvarende i de respektive konfessioner, få adgang til en brev-skabelon fra OneDate.org, der kan medbringes til deres pågældende kirkelige leder. Biskopper / eller tilsvarende i de respektive konfessioner har størst mulighed for at agere aktivt for forandring - og denne stemme fra Kirkens folk ligger inden for deres indflydelsesområde.</p>

<p>Det vil da være hver enkeltes ansvar (uanset fra hvilken konfession) ved at kontakte biskopperne / eller deres tilsvarende indenfor den respektive konfession, at følge op med hensyn til hvilken udvikling der er sket fra deres side. Dette kan virkelig blive enhedens stemme hvis enhver følger disse to enkelte trin.</p>

<p>Den stadige proces er undervejs med hensyn til at kontakte forskellige økumeniske og kirkelige websider og relaterede blogs med henblik på at skabe opmærksomhed om denne appel ved at linke til denne webside.</p>

<p>Du kan hjælpe ved at linke til denne webside og / eller informere dine venner om, at denne appel findes og ved at opfordre dem til at underskrive den.</p>

<p>Supplerende strategier må være i form af at skabe opmærksomhed i kirkerne om vigtigheden af at forene tidspunktet for påske, opfordre til forbøn for at vore kristne ledere må opnå den nødvendige nåde til at gøre tidspunktet for påske til ÉT tidspunkt.</p>

<p>Der er 2 milliarder kristne i verden; vi har alle en opgave at udfylde, der er givet til os af Jesus, med henblik på at tilvejebringe enhed mellem disse 2 milliarder kristne. Ved at underskrive denne appel om fejring af påsken på samme tidspunkt for alle kristne, kan alle vi kristne ud over hele verden gøre noget konkret, der vil hjælpe til at bringe fuld enhed til den ene Kirke, som Jesus grundlagde for 2000 år siden.</p>



<h2>我的投票會有什麼的價值，而這請願書會完成些什麼?</h2>
<p>由於我們「一個日期」這個網站的目的是給平信徒去使用他們領洗時所得到的恩寵 － 天主在伯多祿前書2: 9-10啟示了這恩寵， 「「至於你們，你們卻是特選的種族，王家的司祭，聖潔的國民，屬於主的民族，為叫你們宣揚那由黑暗中召叫你們，進入他奇妙之光者的榮耀。你們從前不是天主的人民，如今卻是天主的人民；從前沒有蒙受愛憐，如今卻蒙受了愛憐。」」 這並不奪去其它天主所給的神恩 － 那些被祝聖的主教們或在相關教派裡同樣的職位，而這是平信徒們在使用天主在他們領洗時所賦予的恩寵，天主也是把這恩寵賜給祂的教會的。</p>

<p style="font-style:italic">教友傳教的義務和權利都由他們和首腦基督的契合而來。事實上，他們因聖洗而被置於基督奧妙的身體內，因堅振而受了聖神德能的強化，被上主委任去從事傳教事業。」 <<教友傳教法令 3>>， 梵蒂岡第二次大公會議文件。</p>

<p>這來自全球基督信徒的請願書就是基督信徒在基督教會的生活上盡他們使徒使命的一種方式。</p>

<p>「 基督是大先知，祂用生活的證據和聖道的能力揭示了天父的王國，並仍在盡祂為先知的任務，直到祂的光榮完全顯示出來。基督不僅藉著聖統，以祂的名義及權力施教，而且也用教友們，讓教友們為祂作見證，以信德的意識及宣道的聖寵訓練他們(宗徒大事錄 2:17-18, 默示錄 19:10)」 <<教會憲章 35>>，梵蒂岡第二次大公會議文件。

</p>

<p>這請願書也代表全球基督信徒在基督教會的生活上與聖統制一起實行他們教導（先知性）的角色。</p>


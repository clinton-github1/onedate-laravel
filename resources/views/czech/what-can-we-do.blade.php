<h2>Jaká je váha mého hlasu a co tato petice dosáhne?</h2>
<p>Cílem našich stránek OneDate je povzbudit laiky, aby prokázali svou křestní milost, kterou Bůh zjevil v 1.listu sv.Petra 2:9-10, “Vy však jste `rod vyvolený, královské kněžstvo, národ svatý, lid náležející Bohu´, abyste hlásali mocné skutky toho, kdo vás povolal ze tmy do svého podivuhodného světla. Kdysi jste `vůbec nebyli lid´, nyní však jste lid Boží; pro vás `nebylo slitování´, ale nyní jste došli slitování” To nepopírá další Boží dary příslušných biskupů a jiných duchovních v jednotlivých denominacích, ale jsou to laici, kteří mají prokázat Bohem darované křestní charisma, které Bůh udělil také Své Církvi.</p>

<p style="font-style:italic">“Povinnost a právo apoštolátu pro laiky se odvozuje přímo z jejich spojení s Kristem jako hlavou. Neboť když byli křtem včleněni do tajemného Těla Kristova a biřmováním utvrzeni silou Ducha svatého, sám Pán je pověřuje apoštolátem.” Odstavec 3, Apoštolát laiků, Druhý vatikánský koncil.</p>

<p>Tato petice křesťanů celého světa má ověřit jejich apoštolát v životě Kristovy Církve.</p>

<p>“ Kristus, veliký prorok, hlásal Otcovo království svědectvím života a mocí slova. Dokud se plně nezjeví ve své slávě, vykonává svůj prorocký úřad nejen prostřčednictvím hierarchie učící jeho jménem a mocí, ale též prostřednictvím laiků; proto sje ustanovuje svými svědky a vybavuje smyslem pro víru a milostí slova(Skutky 2:17-18,Zjevení 19:10)” Odstavec 35, Dokument o Církvi (Lumen Gentium), Druhý vatikánský koncil.</p>

<p>Tato petice prověřuje učitelskou (prorockou) roli laiků celého světa společně s hierarchií v životě Kristovy Církve.</p>

<p>Proto není jen nutné mít důkaz v podobě podpisů, ale petice musí být také dodatečně přijata členy různých denominací, které tvoří církev ve své hierarchii. To se děje v naději, že tato aktivita poslouží jako pádný podnět ke změně pro duchovní, kteří se přesvědčí, že Kristův lid touží demonstrovat jednotu srdce a slavit tento velký svátek jako jedna křesťanská rodina, která není rozštěpena, roztříštěna a rozdělena.</p>

